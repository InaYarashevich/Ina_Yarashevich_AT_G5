package main.java.tasks.homework.basetask;

public class TrainMethodsPrimitive {

    public void printInt(int intNumber) {
        System.out.println("Я получила на вход число " + intNumber);
    }

    public void printLong(long longNumber) {
        System.out.println("Я получила на вход  длинное число " + longNumber);
    }

    public void printChar(char word) {
        System.out.println("Я получила на вход символ " + word);
    }

    public void printFloat(float floatNumber) {
        System.out.println("Я получила на вход дробное число " + floatNumber);
    }

    public void printDouble(double doubleNumber) {
        System.out.println("Я получила на вход длинное дробное число " + doubleNumber);
    }

    public void printShort(short shortNumber) {
        System.out.println("Я получила на вход короткое число " + shortNumber);
    }

    public void printByte(byte bytes) {
        System.out.println("Я получила на вход очень короткое число " + bytes);
    }

    public void printBoolean(boolean booleanValue) {
        System.out.println("Я получила на вход булево " + booleanValue);
    }
}
